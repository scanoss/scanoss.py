import os
import codecs


def read(rel_path):
    """
    Load the specified relative path return the line contents
    :param rel_path: relative file path to load
    :return: file stream reader
    """
    here = os.path.abspath(os.path.dirname(__file__))
    with codecs.open(os.path.join(here, rel_path), 'r') as fp:
        return fp.read()


def get_version(rel_path):
    """
    Extract the version number from the specified file
    :param rel_path: relative path to the file
    :return: version string
    """
    for line in read(rel_path).splitlines():
        if line.startswith('__version__'):
            delim = '"' if '"' in line else "'"
            return line.split(delim)[1]
    else:
        raise RuntimeError('Unable to find version string.')


"""
Load __init__.py from the scanoss package and print the version to stdout
"""
if __name__ == '__main__':
    print(get_version('src/scanoss/__init__.py'))
